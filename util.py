# Support functions
###############################################################################

#######################################
# Convert a dict to a json string
#######################################
def toStr(data):
    if sys.platform == 'linux':
        return json.dumps(json.dumps(data, separators=(',', ':')))

    return json.dumps(data, separators=(',', ':'))